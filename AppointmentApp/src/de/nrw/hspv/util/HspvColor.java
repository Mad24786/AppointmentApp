package de.nrw.hspv.util;

import java.awt.Color;

/**
 * The {@code HspvColor} class is used to encapsulate colors
 * from Corporate Design of HSPV.
 *
 * @version     03 May 2021
 * @author      Mathias Fernahl
 * @see         Color
 *
 */
public class HspvColor {
	
	/**
     * The primary color orange of HSPV.
     */
    public static final Color ORANGE = new Color(255, 105, 10);
    
    /**
     * The primary color gray of HSPV. 
     */
    public static final Color GRAY = new Color(45, 57, 59);
    
    /**
     * The secondary color gray of HSPV.
     */
    public static final Color SEC_GRAY = new Color(128, 128, 128);
    
    /**
     * The secondary color light brown of HSPV.
     */
    public static final Color SEC_BROWN = new Color(217,208,193);
	

}
